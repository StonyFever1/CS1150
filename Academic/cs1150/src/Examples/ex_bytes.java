package chapter2;

public class exampleBytes 
{
    public static void main(String[] args) 
    {
        final double PI = 3.14; // Declare a constant
        byte byteValue = 100; // Declare a variable that is of type byte
        short shortValue = 123; // Declare a variable that is of type integer
        int intValue1 = 123456;
        int intValue2 = -9876543;
        long longValue = 1234567891;
        float floatValue = 71.71f;
        double doubleValue = 12345.234d;
        
        System.out.println("Value of PI = " + PI);
        System.out.println("byte Value = "+ byteValue);
        System.out.println("short Value = "+ shortValue);
        System.out.println("int 1st Value = "+ intValue1);
        System.out.println("int 2nd Value = "+ intValue2);
        System.out.println("long Value = "+ longValue);
        System.out.println("float Value = "+ floatValue);
        System.out.println("double Value = "+ doubleValue);
    }
}